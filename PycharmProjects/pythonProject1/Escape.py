#\'-single quotation-
print("My Name is Priya ,I\'M pursuing BE")     #use for special character or symbol.

#\"-double quotation-
print("My Name is Priya And I\'M enrolled for data science")

#\n-new line
print("My Name is Priya.\nI\'M enrolled for data science")

#\t-add 4 space-
print("My Name is Priya\tAnd I\'M enrolled for data science")

#\b-backspace-

print("My Name is Priya And I\'M enrolled for data science\b")

#\\- if we give double slash then it remains \.
print("My Name is Priya And I\'M enrolled for data science\\ python")
